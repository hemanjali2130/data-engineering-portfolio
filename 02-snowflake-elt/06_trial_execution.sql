CREATE OR REPLACE TASK ANALYTICS.TASK_LOAD_DIMS
  WAREHOUSE=TRANSFORM_WH SCHEDULE='USING CRON */5 * * * * UTC'
  WHEN SYSTEM$STREAM_HAS_DATA('RETAIL_DW.RAW.CUSTOMERS_STREAM')
    OR SYSTEM$STREAM_HAS_DATA('RETAIL_DW.RAW.ORDERS_STREAM')
AS CALL STAGING.SP_MERGE_DIM_CUSTOMER();
CREATE OR REPLACE TASK ANALYTICS.TASK_LOAD_FACTS
  WAREHOUSE=TRANSFORM_WH AFTER ANALYTICS.TASK_LOAD_DIMS
AS CALL ANALYTICS.SP_MERGE_FACT_ORDERS();
