CREATE OR REPLACE PROCEDURE STAGING.SP_MERGE_DIM_CUSTOMER()
RETURNS STRING LANGUAGE SQL AS
$$
BEGIN
  CREATE OR REPLACE TEMPORARY TABLE STAGING._CUST_DELTA AS
  SELECT CUSTOMER_ID,FULL_NAME,EMAIL,PHONE,CITY,STATE,TRY_TO_DATE(SIGNUP_DATE) SIGNUP_DATE,
         TRY_TO_TIMESTAMP(UPDATED_AT) UPDATED_AT
  FROM RAW.CUSTOMERS_STREAM WHERE METADATA$ACTION='INSERT'
  QUALIFY ROW_NUMBER() OVER (PARTITION BY CUSTOMER_ID ORDER BY TRY_TO_TIMESTAMP(UPDATED_AT) DESC)=1;
  MERGE INTO STAGING.DIM_CUSTOMER d USING STAGING._CUST_DELTA s
    ON d.CUSTOMER_ID=s.CUSTOMER_ID AND d.IS_CURRENT
  WHEN MATCHED AND (NOT EQUAL_NULL(d.CITY,s.CITY) OR NOT EQUAL_NULL(d.EMAIL,s.EMAIL)
                    OR NOT EQUAL_NULL(d.PHONE,s.PHONE) OR NOT EQUAL_NULL(d.FULL_NAME,s.FULL_NAME))
    THEN UPDATE SET VALID_TO=s.UPDATED_AT, IS_CURRENT=FALSE;
  INSERT INTO STAGING.DIM_CUSTOMER
    (CUSTOMER_ID,FULL_NAME,EMAIL,PHONE,CITY,STATE,SIGNUP_DATE,VALID_FROM,VALID_TO,IS_CURRENT)
  SELECT s.CUSTOMER_ID,s.FULL_NAME,s.EMAIL,s.PHONE,s.CITY,s.STATE,s.SIGNUP_DATE,
         COALESCE(s.UPDATED_AT,CURRENT_TIMESTAMP()),NULL,TRUE
  FROM STAGING._CUST_DELTA s LEFT JOIN STAGING.DIM_CUSTOMER d
    ON d.CUSTOMER_ID=s.CUSTOMER_ID AND d.IS_CURRENT
  WHERE d.CUSTOMER_ID IS NULL;
  RETURN 'DIM_CUSTOMER merged';
END;
$$;

CREATE OR REPLACE PROCEDURE ANALYTICS.SP_MERGE_FACT_ORDERS()
RETURNS STRING LANGUAGE SQL AS
$$
BEGIN
  MERGE INTO ANALYTICS.FACT_ORDERS f USING (
    SELECT o.ORDER_ID,d.CUSTOMER_SK,TRY_TO_TIMESTAMP(o.ORDER_TS) ORDER_TS,
           TO_DATE(TRY_TO_TIMESTAMP(o.ORDER_TS)) ORDER_DATE,o.STATUS,o.CHANNEL,
           TRY_TO_NUMBER(o.ITEMS) ITEMS,TRY_TO_DECIMAL(o.ORDER_TOTAL,12,2) ORDER_TOTAL
    FROM RAW.ORDERS_STREAM o LEFT JOIN STAGING.DIM_CUSTOMER d
      ON d.CUSTOMER_ID=o.CUSTOMER_ID AND d.IS_CURRENT
    WHERE o.METADATA$ACTION='INSERT'
    QUALIFY ROW_NUMBER() OVER (PARTITION BY o.ORDER_ID ORDER BY TRY_TO_TIMESTAMP(o.ORDER_TS) DESC)=1
  ) s ON f.ORDER_ID=s.ORDER_ID
  WHEN MATCHED THEN UPDATE SET STATUS=s.STATUS,ORDER_TOTAL=s.ORDER_TOTAL,_UPDATED_AT=CURRENT_TIMESTAMP()
  WHEN NOT MATCHED THEN INSERT (ORDER_ID,CUSTOMER_SK,ORDER_TS,ORDER_DATE,STATUS,CHANNEL,ITEMS,ORDER_TOTAL)
    VALUES (s.ORDER_ID,s.CUSTOMER_SK,s.ORDER_TS,s.ORDER_DATE,s.STATUS,s.CHANNEL,s.ITEMS,s.ORDER_TOTAL);
  RETURN 'FACT_ORDERS merged';
END;
$$;

